import { writeFile } from 'node:fs/promises'
import { parse } from 'acorn'

const origin = 'https://www.wutheringtools.com'
const loadText = async (url) => {
  const response = await fetch(url)
  if (!response.ok) throw new Error(`Wuthering Tools ${response.status}: ${url}`)
  return response.text()
}

const propertyName = (property) => property.computed ? undefined : property.key?.name ?? property.key?.value
const staticValue = (node) => {
  if (!node) return undefined
  if (node.type === 'Literal') return node.value
  if (node.type === 'TemplateLiteral' && node.expressions.length === 0) return node.quasis.map((part) => part.value.cooked ?? part.value.raw).join('')
  if (node.type === 'UnaryExpression' && node.operator === '!') return !staticValue(node.argument)
  if (node.type === 'UnaryExpression' && node.operator === '-') {
    const value = staticValue(node.argument)
    return typeof value === 'number' ? -value : undefined
  }
  if (node.type === 'ArrayExpression') return node.elements.map(staticValue)
  if (node.type === 'ObjectExpression') return Object.fromEntries(node.properties.flatMap((property) => {
    if (property.type !== 'Property') return []
    const name = propertyName(property)
    return name === undefined ? [] : [[name, staticValue(property.value)]]
  }))
  return undefined
}

const conditionArrays = (source) => {
  const program = parse(source, { ecmaVersion: 'latest', sourceType: 'module' })
  const conditions = []
  for (const statement of program.body) {
    if (statement.type !== 'VariableDeclaration') continue
    for (const declaration of statement.declarations) {
      if (declaration.init?.type !== 'ArrayExpression') continue
      const isConditionArray = declaration.init.elements.some((element) => element?.type === 'ObjectExpression'
        && element.properties.some((property) => propertyName(property) === 'hasStacks'))
      if (isConditionArray) conditions.push(...staticValue(declaration.init))
    }
  }
  return conditions
}

const cleanDescription = (value = '') => value
  .replace(/<br\s*\/?>/gi, '\n')
  .replace(/<\/(?:div|p|li|h\d)>/gi, '\n')
  .replace(/<[^>]+>/g, '')
  .replace(/&nbsp;/gi, ' ')
  .replace(/&amp;/gi, '&')
  .replace(/&quot;/gi, '"')
  .replace(/&#39;/gi, "'")
  .replace(/\n[ \t]+/g, '\n')
  .replace(/[ \t]{2,}/g, ' ')
  .replace(/\n{3,}/g, '\n\n')
  .trim()

const homepage = await loadText(`${origin}/`)
const entryAssets = [...homepage.matchAll(/<script[^>]+src="([^"]+\.js)"/gi)].map((match) => new URL(match[1], origin).href)
const entrySources = await Promise.all(entryAssets.map(loadText))
const mainSource = entrySources.find((source) => source.includes('"./Aalto/index.ts"'))
if (!mainSource) throw new Error('Could not locate the Wuthering Tools character module map.')

const modules = [...mainSource.matchAll(/"\.\/([^/]+)\/index\.ts":\(\)=>[\s\S]*?import\("\.\/([^"]+\.js)"\)/g)]
  .map((match) => ({ characterKey: match[1], asset: match[2] }))
if (modules.length < 50) throw new Error(`Incomplete Wuthering Tools character module map: ${modules.length}`)

const catalog = {}
for (let offset = 0; offset < modules.length; offset += 10) {
  const batch = modules.slice(offset, offset + 10)
  const rows = await Promise.all(batch.map(async ({ characterKey, asset }) => {
    const source = await loadText(`${origin}/assets/${asset}`)
    const conditions = conditionArrays(source)
      .filter((condition) => condition?.key && !/^Stat Bonus:/i.test(condition.name ?? ''))
      .map((condition) => ({
        key: String(condition.key),
        name: String(condition.name ?? condition.key),
        description: cleanDescription(condition.details),
        hasStacks: Boolean(condition.hasStacks),
        minStacks: Number(condition.minStacks ?? 0),
        maxStacks: Number(condition.maxStacks ?? 0),
        ...(condition.stance ? { stance: String(condition.stance) } : {}),
        ...(condition.appliesOnEveryStep ? { appliesOnEveryStep: Number(condition.appliesOnEveryStep) } : {}),
        sequence: Number(String(condition.key).match(/^(?:SequenceNode|Sequence|ResonanceChain|S)(\d+)/i)?.[1] ?? 0),
        modifiers: Array.isArray(condition.modifiers) ? condition.modifiers : []
      }))
    return [characterKey, conditions]
  }))
  Object.assign(catalog, Object.fromEntries(rows))
}

const generatedAt = new Date().toISOString()
const body = `// Generated by scripts/sync-wutheringtools-conditions.mjs. Do not edit.
export interface GeneratedCharacterConditionModifier {
  modifier?: string
  modifierValue?: number | string | string[] | Record<string, number | string>
  modifySpecificTalents?: string[]
  modifierTalentKey?: string
  modifierValueTalentRef?: string
  maximumValue?: number
  modifierStep?: number
  overflowStep?: number
  overflowMin?: number
  overflowMax?: number
  modifierBasedOn?: string
  modifierTargetAttr?: string
  minStatValue?: number
}
export interface GeneratedCharacterCondition {
  key: string
  name: string
  description: string
  hasStacks: boolean
  minStacks: number
  maxStacks: number
  stance?: string
  appliesOnEveryStep?: number
  sequence: number
  modifiers: GeneratedCharacterConditionModifier[]
}
export const generatedCharacterConditions:Record<string,GeneratedCharacterCondition[]>=${JSON.stringify(catalog, null, 2)}
export const characterConditionProvenance=${JSON.stringify({ source: origin, generatedAt, characterCount: modules.length })} as const
`
await writeFile('src/game-data/character-conditions.generated.ts', body)
console.log(`Wrote ${Object.values(catalog).reduce((total, rows) => total + rows.length, 0)} conditional effects for ${modules.length} characters from Wuthering Tools`)
